public class examples{
    public static void main(String[] args) {
       int a = 29;
       System.out.println(a);

        //octal literal
        int b = 017;
        System.out.println(b);

        //hexa-decimal literals
        int c = 0X243;
        System.out.println(c);

        //binary literals
        int d = 0b1101;
        System.out.println(d);
    }
}